;
; THIS IS A DEMONSTATION PROGRAM OF AN ASSEMBLY	LANGUAGE
; PROGRAM THAT WILL INTERFACE WITH NEVADA BASIC.
;
; THIS ROUTINE IS LOADED AT 8000H AND WILL DOUBLE THE
; NUMBER THAT IS SENT TO IT.
;
	XEQ	8000H
	ORG	8000H
	PUSH	D	;SAVE INPUT NUMBER
	POP	H	;COPY TO HL
	DAD	H	;NOW DOUBLE IT AND RETURN IT IN	HL
	RET
